s1 = {1,2,3,4,5,6}
s2 = {4,5,6,7,8,9}
# union 
print(s1.union(s2)) # it will print all the elements of both set without repeating
print(s1 | s2) # it will print all the elements of both set without repeating
# intersection
print(s1.intersection(s2)) # it will print only common elements of both set